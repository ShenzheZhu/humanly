#!/usr/bin/env node

import path from "node:path";
import {
  DATA_DIR,
  extractJsonObject,
  formatProbability,
  getArg,
  hasFlag,
  makeMockDetectorRow,
  readCsv,
  readSampleText,
  writeDetectorRowsReplacing,
  writeRaw,
} from "./detector_runner_common.mjs";

const DETECTOR = "llm_claude_opus_4_8";
const DEFAULT_SAMPLES_PATH = path.join(DATA_DIR, "samples-generated-pilot-proxy.csv");
const DEFAULT_OUTPUTS_PATH = path.join(DATA_DIR, "detector_outputs_api_pilot.csv");
const SAMPLES_PATH = path.resolve(process.cwd(), getArg("samples", process.env.SAMPLES_PATH || DEFAULT_SAMPLES_PATH));
const OUTPUTS_PATH = path.resolve(
  process.cwd(),
  getArg("outputs", process.env.DETECTOR_OUTPUTS_PATH || DEFAULT_OUTPUTS_PATH),
);
const LIMIT = Number(getArg("limit", process.env.DETECTOR_LIMIT || "0"));
const DRY_RUN = hasFlag("dry-run") || hasFlag("mock");
const PROVIDER = getArg(
  "provider",
  process.env.LLM_DETECTOR_PROVIDER || (process.env.OPENROUTER_API_KEY ? "openrouter" : "anthropic"),
);
const MODEL = getArg(
  "model",
  process.env.LLM_DETECTOR_MODEL ||
    process.env.ANTHROPIC_MODEL ||
    (PROVIDER === "openrouter" ? "anthropic/claude-opus-4.8" : "claude-opus-4-8"),
);
const ENDPOINT = getArg(
  "endpoint",
  process.env.LLM_DETECTOR_API_URL ||
    process.env.ANTHROPIC_API_URL ||
    (PROVIDER === "openrouter" ? "https://openrouter.ai/api/v1/chat/completions" : "https://api.anthropic.com/v1/messages"),
);
const ANTHROPIC_VERSION = process.env.ANTHROPIC_VERSION || "2023-06-01";
const MAX_TOKENS = Number(process.env.LLM_DETECTOR_MAX_TOKENS || "320");
const PROGRESS_EVERY = Number(process.env.LLM_DETECTOR_PROGRESS_EVERY || "10");
const THRESHOLD_RULE =
  "Unified detector scale: HUMAN_ONLY => human_compliant; MIXED or AI_ONLY => ai_suspicious for the current policy-error analysis.";

const SYSTEM_PROMPT = `You are a final-text-only AI-writing detector predictor for a research evaluation.

You do not have access to keystroke logs, paste events, cursor movements, or writing-process evidence.
Judge only the final text shown by the user. Do not decide whether a writing policy was followed. Predict the apparent document-level authorship class on the same scale used by commercial final-text detectors.

Use exactly one document_classification:
- HUMAN_ONLY: the final text appears written only by a human.
- MIXED: the final text appears to contain both human-written and AI-assisted or AI-generated portions.
- AI_ONLY: the final text appears substantially generated by AI.

Return calibrated class probabilities for all three classes. The probabilities should sum to 1.0.

Return only compact JSON with this exact shape:
{"document_classification":"HUMAN_ONLY"|"MIXED"|"AI_ONLY","class_probabilities":{"human":0.0-1.0,"mixed":0.0-1.0,"ai":0.0-1.0},"confidence_category":"low"|"medium"|"high"}

Do not include markdown, prose outside JSON, or additional keys.`;

function usage() {
  return `Usage:
  node data/detector-stress-test/scripts/run_llm_detector_baseline.mjs --dry-run
  ANTHROPIC_API_KEY=... node data/detector-stress-test/scripts/run_llm_detector_baseline.mjs

Options:
  --samples=<path>   sample manifest CSV
  --outputs=<path>   normalized detector output CSV
  --limit=<n>        optional sample limit
  --provider=<name>  anthropic or openrouter
  --model=<id>       default: ${MODEL}
  --dry-run          deterministic local mock, no network`;
}

function promptForText(text) {
  return `Final text to classify:

<final_text>
${text.trim()}
</final_text>`;
}

function normalizeDocumentClass(value, classProbabilities) {
  const label = String(value || "").trim().toUpperCase();
  if (["HUMAN_ONLY", "MIXED", "AI_ONLY"].includes(label)) return label;
  const entries = Object.entries(classProbabilities).sort((left, right) => Number(right[1]) - Number(left[1]));
  const top = entries[0]?.[0] || "human";
  if (top === "ai") return "AI_ONLY";
  if (top === "mixed") return "MIXED";
  return "HUMAN_ONLY";
}

function normalizeClassProbabilities(value) {
  const raw = value || {};
  const probabilities = {
    human: Number(raw.human ?? raw.HUMAN_ONLY ?? raw.human_only ?? 0),
    mixed: Number(raw.mixed ?? raw.MIXED ?? 0),
    ai: Number(raw.ai ?? raw.AI_ONLY ?? raw.ai_only ?? 0),
  };
  for (const key of Object.keys(probabilities)) {
    if (!Number.isFinite(probabilities[key]) || probabilities[key] < 0) probabilities[key] = 0;
  }
  const total = probabilities.human + probabilities.mixed + probabilities.ai;
  if (total <= 0) return { human: 0.34, mixed: 0.33, ai: 0.33 };
  return {
    human: probabilities.human / total,
    mixed: probabilities.mixed / total,
    ai: probabilities.ai / total,
  };
}

function binaryPredictionFromDocumentClass(documentClass) {
  return documentClass === "HUMAN_ONLY" ? "human_compliant" : "ai_suspicious";
}

function aiSuspiciousProbability(classProbabilities) {
  return Number(classProbabilities.mixed || 0) + Number(classProbabilities.ai || 0);
}

async function callAnthropic(text, apiKey) {
  const response = await fetch(ENDPOINT, {
    method: "POST",
    headers: {
      "Content-Type": "application/json",
      "x-api-key": apiKey,
      "anthropic-version": ANTHROPIC_VERSION,
    },
    body: JSON.stringify({
      model: MODEL,
      max_tokens: MAX_TOKENS,
      system: SYSTEM_PROMPT,
      messages: [{ role: "user", content: [{ type: "text", text: promptForText(text) }] }],
    }),
  });
  const responseText = await response.text();
  let raw;
  try {
    raw = responseText ? JSON.parse(responseText) : {};
  } catch {
    raw = { raw_response_text: responseText };
  }
  if (!response.ok) {
    const error = new Error(`HTTP ${response.status}`);
    error.status = response.status;
    error.body = raw;
    throw error;
  }
  return raw;
}

async function callOpenRouter(text, apiKey) {
  const response = await fetch(ENDPOINT, {
    method: "POST",
    headers: {
      "Content-Type": "application/json",
      Authorization: `Bearer ${apiKey}`,
      "HTTP-Referer": "https://writehumanly.net",
      "X-Title": "Humanly Detector Evaluation",
    },
    body: JSON.stringify({
      model: MODEL,
      max_tokens: MAX_TOKENS,
      messages: [
        {
          role: "user",
          content: `${SYSTEM_PROMPT}\n\n${promptForText(text)}`,
        },
      ],
    }),
  });
  const responseText = await response.text();
  let raw;
  try {
    raw = responseText ? JSON.parse(responseText) : {};
  } catch {
    raw = { raw_response_text: responseText };
  }
  if (!response.ok) {
    const error = new Error(`HTTP ${response.status}`);
    error.status = response.status;
    error.body = raw;
    throw error;
  }
  return raw;
}

function assistantText(raw) {
  if (raw.choices?.[0]?.message?.content) return String(raw.choices[0].message.content).trim();
  return (raw.content || [])
    .map((part) => (part.type === "text" ? part.text : ""))
    .filter(Boolean)
    .join("\n")
    .trim();
}

if (hasFlag("help") || hasFlag("h")) {
  console.log(usage());
  process.exit(0);
}

const allSamples = await readCsv(SAMPLES_PATH);
const samples = LIMIT > 0 ? allSamples.slice(0, LIMIT) : allSamples;
const timestamp = new Date().toISOString();
const rows = [];
const apiKey = PROVIDER === "openrouter" ? process.env.OPENROUTER_API_KEY : process.env.ANTHROPIC_API_KEY;

if (!DRY_RUN && !apiKey) {
  throw new Error(
    `${PROVIDER === "openrouter" ? "OPENROUTER_API_KEY" : "ANTHROPIC_API_KEY"} is missing; use --dry-run for a no-network plumbing run.`,
  );
}

for (const [index, sample] of samples.entries()) {
  const text = await readSampleText(sample);
  if (DRY_RUN) {
    const { raw, row } = makeMockDetectorRow({
      detector: DETECTOR,
      detectorVersion: `${MODEL}:mock-dry-run`,
      sample,
      text,
      timestamp,
      thresholdRule: `Mock dry run only; live rule would be: ${THRESHOLD_RULE}`,
    });
    await writeRaw(DETECTOR, sample.sample_id, raw);
    rows.push(row);
    continue;
  }

  try {
    const raw = PROVIDER === "openrouter" ? await callOpenRouter(text, apiKey) : await callAnthropic(text, apiKey);
    const parsed = extractJsonObject(assistantText(raw));
    const classProbabilities = normalizeClassProbabilities(parsed.class_probabilities);
    const documentClass = normalizeDocumentClass(parsed.document_classification, classProbabilities);
    const binaryPrediction = binaryPredictionFromDocumentClass(documentClass);
    const aiProbability = aiSuspiciousProbability(classProbabilities);
    const storedRaw = { anthropic_response: raw, parsed_detector_json: parsed };
    await writeRaw(DETECTOR, sample.sample_id, storedRaw);
    rows.push({
      sample_id: sample.sample_id,
      detector: DETECTOR,
      detector_version: raw.model || raw.provider || MODEL,
      run_timestamp_utc: timestamp,
      raw_label: documentClass,
      canonical_document_class: documentClass,
      class_probabilities_json: JSON.stringify(classProbabilities),
      confidence_category: parsed.confidence_category ?? "",
      raw_score_json: JSON.stringify({
        document_classification: documentClass,
        class_probabilities: classProbabilities,
        ai_probability: aiProbability,
        confidence_category: parsed.confidence_category ?? "",
      }),
      ai_probability: formatProbability(aiProbability),
      binary_prediction: binaryPrediction,
      threshold_rule: THRESHOLD_RULE,
      request_status: "success",
      error_notes: "",
    });
  } catch (error) {
    const raw = { error: error.message, http_status: error.status || "", body: error.body || null };
    await writeRaw(DETECTOR, sample.sample_id, raw);
    rows.push({
      sample_id: sample.sample_id,
      detector: DETECTOR,
      detector_version: MODEL,
      run_timestamp_utc: timestamp,
      raw_label: "",
      canonical_document_class: "",
      class_probabilities_json: "",
      confidence_category: "",
      raw_score_json: JSON.stringify(raw),
      ai_probability: "",
      binary_prediction: "",
      threshold_rule: THRESHOLD_RULE,
      request_status: "api_error",
      error_notes: error.message,
    });
  }
  if (!DRY_RUN && PROGRESS_EVERY > 0 && ((index + 1) % PROGRESS_EVERY === 0 || index + 1 === samples.length)) {
    console.error(`${DETECTOR}: processed ${index + 1}/${samples.length}`);
  }
}

await writeDetectorRowsReplacing(OUTPUTS_PATH, DETECTOR, samples, rows);
console.log(`${DETECTOR}: wrote ${rows.length} row(s) to ${path.relative(process.cwd(), OUTPUTS_PATH)}`);

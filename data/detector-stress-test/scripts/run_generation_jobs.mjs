function syntheticTranslation(job, inputText, prompt) {
  const target = targetWordCountFromPrompt(prompt);
  const bucket = lengthBucketFromJob(job);
  const intro =
    bucket === "long"
      ? "The source passage presents a sustained human-written discussion in another language. This proxy translation preserves the role of a translated human source for pipeline testing, while avoiding any claim that it is a verified literary translation."
      : bucket === "medium"
        ? "The source passage is human-written text in another language. This proxy translation keeps the construction path visible for testing the translation condition."
        : "This is a proxy translation of human-written non-English text for the translation condition.";
  const sourceWords = inputText.split(/\s+/).slice(0, 18).join(" ");
  const paragraphs = [
    `${intro} The passage begins from material such as: ${sourceWords}.`,
    "In the benchmark, this case represents a policy-compliant path where the substantive content originates with a human writer and AI is used only to translate it into English. A final-text detector may still flag the result because translated prose can sound unusually smooth, formal, or detached from ordinary drafting habits.",
    "The important point is not the literary content itself, but the mismatch between surface style and process. A process-aware system would record that the English text came from a non-English human source through an allowed translation step, while a text-only detector can only infer from the final wording.",
  ];
  return expandToLength(paragraphs, target);
}

function syntheticHumanize(inputText, prompt) {
  const target = targetWordCountFromPrompt(prompt);
  const cleaned = lightEdit(inputText)
    .replace(/\bprocess evidence\b/gi, "a record of the writing process")
    .replace(/\bpolicy compliance\b/gi, "whether the rules were followed")
    .replace(/\bsubstantive\b/gi, "main")
    .replace(/\bauditable\b/gi, "possible to review");
  return expandToLength([cleaned], Math.min(target, Math.max(80, wordCount(cleaned))));
}

function syntheticChatCompletion(prompt, job, inputText) {
  if (job.case_id === "C2") return syntheticHumanize(inputText, prompt);
  if (job.case_id === "C3") return syntheticTranslation(job, inputText, prompt);
  if (job.case_id === "N2") return syntheticHumanize(inputText, prompt);
  if (job.case_id === "N3" && /translate/i.test(job.job_id)) {
    return syntheticEnglish(job, prompt);
  }
  if (job.case_id === "N3") return syntheticChinese(job, prompt);
  return syntheticEnglish(job, prompt);
}

function requestBodyForChatCompletion({ model, prompt }) {
  const body = {
    model,
    messages: [{ role: "user", content: prompt }],
  };
  if (Object.hasOwn(process.env, "GENERATION_TEMPERATURE")) {
    const temperature = process.env.GENERATION_TEMPERATURE;
    if (temperature && !["default", "omit"].includes(temperature.toLowerCase())) {
      body.temperature = Number(temperature);
    }
  }
  if (process.env.GENERATION_MAX_COMPLETION_TOKENS) {
    body.max_completion_tokens = Number(process.env.GENERATION_MAX_COMPLETION_TOKENS);
  } else if (process.env.GENERATION_MAX_TOKENS) {
    body.max_tokens = Number(process.env.GENERATION_MAX_TOKENS);
  }
  return body;
}

let lastRequestStartedAt = 0;

async function throttleChatCompletion() {
  if (!requestIntervalMs || requestIntervalMs <= 0) return;
  const elapsed = Date.now() - lastRequestStartedAt;
  if (lastRequestStartedAt && elapsed < requestIntervalMs) {
    await sleep(requestIntervalMs - elapsed);
  }
  lastRequestStartedAt = Date.now();
}

function retryDelayMs(response, rawJson, rawText, attempt) {
  const retryAfter = response.headers.get("retry-after");
  if (retryAfter) {
    const seconds = Number(retryAfter);
    if (Number.isFinite(seconds) && seconds > 0) return seconds * 1000;
  }
  const message = String(
    rawJson?.error?.message ||
      rawJson?.message ||
      rawJson?.raw_response_text ||
      rawText ||
      "",
  );
  const match = message.match(/try again in\s+([\d.]+)\s*(ms|s)\b/i);
  if (match) {
    const amount = Number(match[1]);
    if (Number.isFinite(amount)) return match[2].toLowerCase() === "ms" ? amount : amount * 1000;
  }
  return Math.min(60_000, 2000 * 2 ** attempt);
}

function isRetryableRateLimit(rawJson, rawText) {
  const message = String(
    rawJson?.error?.message ||
      rawJson?.message ||
      rawJson?.raw_response_text ||
      rawText ||
      "",
  ).toLowerCase();
  return !(
    message.includes("requests per day") ||
    message.includes("(rpd)") ||
    message.includes("adding a payment method")
  );
}

async function chatCompletion(prompt, jobId) {
  const apiKey =
    process.env.GENERATION_API_KEY || process.env.OPENAI_API_KEY || process.env.AI_API_KEY;
  const model = process.env.GENERATION_MODEL || process.env.AI_MODEL;
  const baseUrl =
    process.env.GENERATION_BASE_URL ||
    process.env.OPENAI_BASE_URL ||
    process.env.AI_BASE_URL ||
    "https://api.openai.com/v1";
  if (!apiKey) {
    throw new Error("GENERATION_API_KEY, OPENAI_API_KEY, or AI_API_KEY is missing");
  }
  if (!model) {
    throw new Error("GENERATION_MODEL or AI_MODEL is missing");
  }

  const endpoint = `${baseUrl.replace(/\/$/, "")}/chat/completions`;
  let lastError = null;
  for (let attempt = 0; attempt <= maxRetries; attempt += 1) {
    await throttleChatCompletion();
    const response = await fetch(endpoint, {
      method: "POST",
      headers: {
        Authorization: `Bearer ${apiKey}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify(requestBodyForChatCompletion({ model, prompt })),
    });
    const rawText = await response.text();
    await mkdir(RAW_DIR, { recursive: true });
    const rawPath = path.join(RAW_DIR, `${jobId}.json`);
    const rawRelativePath = path.relative(DATA_DIR, rawPath);
    let rawJson;
    try {
      rawJson = JSON.parse(rawText);
      await writeFile(rawPath, `${JSON.stringify(rawJson, null, 2)}\n`);
    } catch {
      rawJson = { raw_response_text: rawText };
      await writeFile(rawPath, `${rawText}\n`);
    }
    if (response.ok) {
      const content = rawJson?.choices?.[0]?.message?.content;
      if (!content?.trim()) {
        throw new Error(`generation API returned no message content for ${jobId}`);
      }
      return {
        content: content.trim(),
        metadata: {
          provider: process.env.GENERATION_PROVIDER || "openai_compatible",
          base_url: baseUrl,
          request_model: model,
          response_model: rawJson?.model || "",
          raw_response_path: rawRelativePath,
          usage: rawJson?.usage || {},
          retry_attempts: attempt,
        },
      };
    }
    lastError = new Error(`generation API HTTP ${response.status}: ${rawText.slice(0, 240)}`);
    if (
      response.status !== 429 ||
      !isRetryableRateLimit(rawJson, rawText) ||
      attempt >= maxRetries
    ) {
      break;
    }
    const delay = retryDelayMs(response, rawJson, rawText, attempt);
    console.warn(`${jobId}: rate limited; retrying in ${Math.ceil(delay / 1000)}s (attempt ${attempt + 1}/${maxRetries})`);
    await sleep(delay);
  }
  throw lastError;
}

async function dependencyOutputsExist(job) {
  for (const dependencyJobId of job.dependency_job_ids || []) {
    const dependency = allJobsById.get(dependencyJobId);
    if (!dependency) return false;
    if (!(await exists(path.join(DATA_DIR, dependency.output_text_path)))) return false;
  }
  return true;
}

await loadDotenv();

const allJobs = loadJobs(await readFile(JOBS_PATH, "utf8"));
const allJobsById = new Map(allJobs.map((job) => [job.job_id, job]));
let jobs = allJobs;
if (selectedJobId) jobs = jobs.filter((job) => job.job_id === selectedJobId);
if (selectedSampleId) jobs = jobs.filter((job) => job.sample_id === selectedSampleId);
if (!jobs.length) {
  throw new Error(`No jobs matched selection. ${usage()}`);
}

const results = [];
let processed = 0;
for (const [jobIndex, job] of jobs.entries()) {
  if (processed >= limit) break;
  if (progressEvery > 0 && (jobIndex === 0 || (jobIndex + 1) % progressEvery === 0)) {
    console.log(`progress: checking job ${jobIndex + 1}/${jobs.length} (${job.job_id})`);
  }
  if (!(await dependencyOutputsExist(job))) {
    results.push({ job, status: "pending_dependency", notes: "dependency output missing" });
    continue;
  }

  const inputText = job.input_text_path ? await readRelative(job.input_text_path) : "";
  const prompt = renderPrompt(job.prompt_template || "", inputText);
  const inputMeta = {
    input_text_path: job.input_text_path || "",
    input_text_sha256: sha256(inputText),
    prompt_template_sha256: sha256(job.prompt_template || ""),
    rendered_prompt_sha256: sha256(prompt),
  };

  const outputPath = path.join(DATA_DIR, job.output_text_path);
  if (await exists(outputPath)) {
    const outputMeta = await readJsonRelativeIfExists(`${job.output_text_path}.meta.json`);
    const sourceMatches =
      outputMeta?.input_text_sha256 === inputMeta.input_text_sha256 &&
      outputMeta?.prompt_template_sha256 === inputMeta.prompt_template_sha256 &&
      outputMeta?.rendered_prompt_sha256 === inputMeta.rendered_prompt_sha256;
    const shouldForce =
      force || !sourceMatches || (forceProxyOnly && outputMeta?.generation_mode === "synthetic_proxy");
    if (!shouldForce) {
      results.push({ job, status: "already_done", notes: "input hash matches existing output" });
      continue;
    }
  }

  if (dryRun) {
    results.push({
      job,
      status: "dry_run_ready",
      notes: job.requires_api ? "would call chat completion API" : "would run local scripted edit",
    });
    processed += 1;
    continue;
  }

  try {
    let output;
    let extraMeta = {};
    if (job.job_type === "chat_completion") {
      if (syntheticProxy) {
        output = syntheticChatCompletion(prompt, job, inputText);
      } else {
        const generated = await chatCompletion(prompt, job.job_id);
        output = generated.content;
        extraMeta = generated.metadata;
      }
    } else if (job.job_type === "scripted_light_edit") {
      output = lightEdit(inputText);
    } else {
      throw new Error(`Unsupported job_type ${job.job_type}`);
    }
    const status = syntheticProxy ? "synthetic_proxy_success" : "success";
    await writeOutputWithMeta(job, output, status, { ...inputMeta, ...extraMeta });
    results.push({ job, status, notes: syntheticProxy ? "offline synthetic proxy output; not paper-ready generation" : "" });
    console.log(`${job.job_id}: ${status} -> ${job.output_text_path}`);
    processed += 1;
  } catch (error) {
    results.push({ job, status: "error", notes: error.message });
    console.error(`${job.job_id}: ${error.message}`);
    processed += 1;
  }
}


function csvEscape(value) {
  const stringValue = value == null ? "" : String(value);
  if (/[",\n]/.test(stringValue)) {
    return `"${stringValue.replace(/"/g, '""')}"`;
  }
  return stringValue;
}

async function loadDotenv() {
  const envPath = path.join(process.cwd(), ".env");
  if (!(await exists(envPath))) return;
  const text = await readFile(envPath, "utf8");
  for (const line of text.split(/\n/)) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith("#") || !trimmed.includes("=")) continue;
    const index = trimmed.indexOf("=");
    const key = trimmed.slice(0, index).trim();
    let value = trimmed.slice(index + 1).trim();
    if (
      (value.startsWith('"') && value.endsWith('"')) ||
      (value.startsWith("'") && value.endsWith("'"))
    ) {
      value = value.slice(1, -1);
    }
    if (!process.env[key]) process.env[key] = value;
  }
}

async function exists(filePath) {
  try {
    await stat(filePath);
    return true;
  } catch {
    return false;
  }
}

async function readRelative(relativePath) {
  return readFile(path.join(DATA_DIR, relativePath), "utf8");
}

async function readJsonRelativeIfExists(relativePath) {
  const absolutePath = path.join(DATA_DIR, relativePath);
  if (!(await exists(absolutePath))) return null;
  return JSON.parse(await readFile(absolutePath, "utf8"));
}

async function writeRelative(relativePath, text) {
  const absolutePath = path.join(DATA_DIR, relativePath);
  await mkdir(path.dirname(absolutePath), { recursive: true });
  await writeFile(absolutePath, text.endsWith("\n") ? text : `${text}\n`);
}

async function writeOutputWithMeta(job, output, status, extraMeta = {}) {
  await writeRelative(job.output_text_path, output);
  const meta = {
    job_id: job.job_id,
    sample_id: job.sample_id,
    case_id: job.case_id,
    job_type: job.job_type,
    generation_mode: syntheticProxy ? "synthetic_proxy" : "api_or_script",
    status,
    generated_at_utc: new Date().toISOString(),
    notes: syntheticProxy
      ? "Offline synthetic proxy output for pipeline testing; not paper-ready generation."
      : "Generated by live API or local scripted edit.",
    ...extraMeta,
  };
  await writeRelative(`${job.output_text_path}.meta.json`, JSON.stringify(meta, null, 2));
}

function loadJobs(text) {
  return text
    .split(/\n+/)
    .map((line) => line.trim())
    .filter(Boolean)
    .map((line) => JSON.parse(line));
}

function renderPrompt(template, inputText) {
  return template.replaceAll("{{INPUT_TEXT}}", inputText.trim());
}

function sha256(text) {
  return crypto.createHash("sha256").update(text).digest("hex");
}

function lightEdit(text) {
  return text
    .replace(/\bDo not\b/g, "Don't")
    .replace(/\bdo not\b/g, "don't")
    .replace(/\bIt is important to note that\s*/gi, "")
    .replace(/\bAdditionally,\s*/g, "")
    .replace(/\bFurthermore,\s*/g, "")
    .replace(/\bHowever,\s*/g, "But ")
    .replace(/\btherefore\b/gi, "so")
    .replace(/\butilize\b/gi, "use")
    .replace(/\bleverage\b/gi, "use")
    .replace(/\s+,/g, ",")
    .replace(/[ \t]{2,}/g, " ")
    .replace(/\n{3,}/g, "\n\n")
    .trim();
}

function targetWordCountFromPrompt(prompt) {
  if (/1000\D{0,30}1500/i.test(prompt)) return 1120;
  if (/400\D{0,30}600/i.test(prompt)) return 470;
  if (/120\D{0,30}180/i.test(prompt)) return 150;
  return 300;
}

function lengthBucketFromJob(job) {
  const match = job.sample_id.match(/_(short|medium|long)_/);
  return match?.[1] || "medium";
}

function expandToLength(paragraphs, targetWords) {
  const out = [];
  let index = 0;
  const maxIterations = Math.max(20, targetWords * 2);
  while (wordCount(out.join("\n\n")) < targetWords && index < maxIterations) {
    out.push(paragraphs[index % paragraphs.length]);
    index += 1;
  }
  return out.join("\n\n");
}

function wordCount(text) {
  const latinLikeWords = text.match(/\b[\p{Script=Latin}\p{M}\p{N}’'-]+\b/gu) || [];
  const cjkChars =
    text.match(/[\p{Script=Han}\p{Script=Hiragana}\p{Script=Katakana}\p{Script=Hangul}]/gu) || [];
  return latinLikeWords.length + Math.ceil(cjkChars.length / 2);
}

function sleep(ms) {
  return new Promise((resolve) => setTimeout(resolve, ms));
}

